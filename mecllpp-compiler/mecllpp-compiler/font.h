/* ================================================================
 * font_get_glyph_ascii()
 *   ASCII karakterin glyph satır dizisini döndürür.
 *   Tanımsız karakter için space döner.
 * ================================================================ */
static inline const uint8_t_f *font_get_glyph_ascii(unsigned char c)
{
    if (c >= 0x20 && c <= 0x7E)
        return font_ascii[c - 0x20];
    return font_ascii[0]; /* space */
}

/* ================================================================
 * font_get_glyph_tr()
 *   Türkçe karakter glyph satır dizisini döndürür.
 *   idx: FONT_TR_xxx sabitlerinden biri.
 * ================================================================ */
static inline const uint8_t_f *font_get_glyph_tr(int idx)
{
    if (idx >= 0 && idx < FONT_TR_COUNT)
        return font_tr[idx];
    return font_ascii[0]; /* space */
}

